function openPlaylist(naam) {
  document.getElementById("playlist-titel").textContent = titels[naam];
  document.getElementById("overzicht").style.display = "none";
  document.getElementById("playlist-view").style.display = "block";
  laadLiedjes(naam);
}

function laadLiedjes(naam) {
  var container = document.getElementById("playlist-liedjes");
  var liedjes = afspeellijsten[naam];

  container.innerHTML = "";

  for (var i = 0; i < liedjes.length; i++) {
    var liedje = liedjes[i];

    var div = document.createElement("div");
    div.className = "track";

    div.innerHTML =
      '<span class="track-number">' +
      liedje.nummer +
      "</span>" +
      '<img src="' +
      liedje.cover +
      '" alt="' +
      liedje.naam +
      '">' +
      '<div class="track-info">' +
      '<p class="track-name">' +
      liedje.naam +
      "</p>" +
      '<p class="track-artist">' +
      liedje.artiest +
      "</p>" +
      "</div>" +
      '<button class="like-btn liked" data-liked="true">♥</button>';

    var likeKnop = div.querySelector(".like-btn");

    likeKnop.addEventListener("click", function (e) {
      var knop = e.currentTarget;
      if (knop.dataset.liked == "true") {
        knop.dataset.liked = "false";
        knop.textContent = "♡";
        knop.classList.remove("liked");
      } else {
        knop.dataset.liked = "true";
        knop.textContent = "♥";
        knop.classList.add("liked");
      }
    });

    container.appendChild(div);
  }
}

function sluitPlaylist() {
  document.getElementById("overzicht").style.display = "block";
  document.getElementById("playlist-view").style.display = "none";
}

